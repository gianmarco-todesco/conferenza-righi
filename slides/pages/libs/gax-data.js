class PolychoronData {
    constructor(vertices, faces, cells) {
        let i
        this.vertices = []
        for(i=0; i+3<vertices.length; i+= 4) {
            this.vertices.push(new BABYLON.Vector4(
                vertices[i],vertices[i+1],vertices[i+2],vertices[i+3]
            ))
        }
        i = 0
        this.faces = []
        while(i<faces.length)
        {
            let m = faces[i]
            this.faces.push(faces.slice(i+1,i+m+1))
            i += m + 1
        }
        i = 0
        this.cells = []
        while(i<cells.length)
        {
            let m = cells[i]
            this.cells.push(cells.slice(i+1,i+m+1))
            i += m + 1
        }
        this.edges = []
        let td = {}
        this.faces.forEach(f => {
            let a = f[f.length-1]
            f.forEach(b => {
                const id = a + "," + b
                if(td[id]===undefined) {
                    td[id] = td[b+','+a] = true
                    this.edges.push([a,b])                    
                }
                a = b
            })
        })            
    }
}

